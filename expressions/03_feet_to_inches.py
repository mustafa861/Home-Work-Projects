# Converts feet to inches. Feet is an American unit of measurement. There are 12 inches per foot.
#  Foot is the singular, and feet is the plural.



    
feet = float(input("Enter the number of feet to convert: "))
inches = feet * 12  
    

print("\n=== Feet to Inches Conversion ===")
print(f"Feet: {feet}")
print("1 Foot = 12 Inches")
print(f"Total Inches: {inches}")

